    private void cmdSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSimpanActionPerformed
        String tNama=txtNama.getText();
        String tUser=txtUser.getText();
        String tPassword=txtPassword.getText();
        try {
            if(edit == true) {
                stm.executeUpdate("update user set name='"+tNama+"',password='"+tPassword+"' where user_id='" + tUser + "'");

            } else {
                stm.executeUpdate("INSERT into user VALUES('"+tUser+"','"+tNama+"','"+tPassword+"')");
            }
            tblUser.setModel(new DefaultTableModel(dataTable,header));
            baca_data();
            aktif(false);
            setTombol(true);
            kosong();
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e + "Salah disini");
        }
    }//GEN-LAST:event_cmdSimpanActionPerformed

