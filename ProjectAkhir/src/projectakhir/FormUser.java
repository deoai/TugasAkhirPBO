    private void baca_data() {
        try {
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsUser = stm.executeQuery("select * from user");

            ResultSetMetaData meta = RsUser.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0; 
            while(RsUser.next()) {
                baris = RsUser.getRow();
            } 
            dataTable = new Object[baris][col];
            int x = 0;
            RsUser.beforeFirst();
            while(RsUser.next()) {
                dataTable[x][0] = RsUser.getString("name");
                dataTable[x][1] = RsUser.getString("user_id");
                dataTable[x][2] = RsUser.getString("password");
                x++;
            } 
            tblUser.setModel(new DefaultTableModel(dataTable,header)); 
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    } 
    private void setField() {
        int row=tblUser.getSelectedRow();
        txtNama.setText((String)tblUser.getValueAt(row,0));
        txtUser.setText((String)tblUser.getValueAt(row,1));
        txtPassword.setText((String)tblUser.getValueAt(row,2));
    }
